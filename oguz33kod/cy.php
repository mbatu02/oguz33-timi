<p>Ben ile özellikle murat arkadaşım, köy yerinde çamurun ve pisliğin içerisindeki;
—burunları sümüklü, kırmızı yanaklı ve
—yüzleri soluk çocuklarla, kendi aramızda çeşitli oyunlar oynar, bil hasada küçük yaştaki, kız ve erkek çocuklarına birkaç kelimede olsa kendimizce Türkçe bir şeyler öğretirdik, birkaç deneme ile hemen öğrenirlerdi de. Çünkü kendileri istiyordu, maşallah cin gibi çocuklardı, bir yerde, hiç kimsenin gitmek istemediği, gözlerden ırak kör noktadaki bu köyde, bir nevi şark hizmeti ve gönüllü öğretmenlik, yapıyor gibiydik.
Kuzularına çobanlık yapan, 8-10 yaş arası, bazı küçük erkek çocuklarına arazide denk gelirdik, sohbetler sırasında, sorduğumuz sorulara, az çok yarım yamalak bozuk bir Türkçeyle;
—ben Tırki (Türkçe) biliyorum diyerekten,
sabırsızca hemen suallerimize yüksek sesle cevap verirlerdi;
—benim babam hayvanlık yapar,
—köy yerinde kolik (dana yavrusu) alır ve kolik satar diye,
insanı güldüren komik ve esprili durumlarda, arazide soğuktan yanakları al al olmuş, davarlarını güden küçük yaştaki çobanlarla, yapmış olduğumuz bu güzel ve bir o kadarda keyifli sohbetlerde, haliyle kendiliğinden ortaya çıkan durumlardı.</p>
