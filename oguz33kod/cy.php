<p>Terör belası yüzünden, adeta cehaletin pençesine bu sınır boyları bırakılarak, devlet eliyle yöre insanı kaderlerine, bir şekilde terk edilmiş, özelliklede çocuk yaştaki gençler, örgütün tuzaklarına düşerek, devletine düşman edilmiştir.
Belki de, kiminin evinde tek lokma ekmek bulunmuyordu, işsizdi, ne yapacağını bilemez haldeydi, üstüne üstlük devletin şefkatinden uzak kalmışlardı, bu türden bir yaşama katlanmak zorunda bulunan, çoğu çocuk yaşlarındaki gençler de, babaları gurbette olduklarından ve kendilerini, bulundukları hayata bağlayan, hiçbir neden bulamadıklarından, bölgede oynanan oyunlara kanarak; kendilerini dağlarda buluyorlardı.)
Köy yerindeki çocuklara, beraberimizde getirdiğimiz fazla kumanyalarımız, daha doğrusu, genelde askerlerimizce çöpe atılacak, istenmeyen kumanyaları ve bazı ezme türü yiyeceklerimizi, köy yerindeki, bu masum yüzlü, dünya tatlısı yanakları, al kırmızısı ve burunları, sümüklü çocuklara vererek, kendimizi onlara sevdirmesini bilirdik.
Yakın olurduk kendilerine, birlikte bazı oyunlar oynar, şarkılar söyler, beraberce toplu fotoğraflar dahi çektirirdik, bizleri severlerdi, her fırsatta da, elleriyle selamlarını çakarak, bu sevgilerini belli ettirirlerdi. Köy yerindeki bu çocuklardan birisinin babasıyla, yakinen tanıştık, kendisi açıktan olmasa da, tam bir asker ve tam bir devlet tarafıydı, hatta insanı mahcup edecek derecede, bir gece benle murat arkadaşımı misafir olarak, evlerinde yatıya davet etmişti. Muhabbeti bol güzel bir geceden sonra, sabahta asker deyimiyle kebap gibi, sıcak tandır ekmeği, pekmez ve köye özgü, peynir çeşitleriyle doğal tatlardan oluşan, çok güzel bir kahvaltı, ziyafeti yapmıştık.</p>
