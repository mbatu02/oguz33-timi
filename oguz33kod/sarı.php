yazılı kimlik bilgilerimi A4 kağıdıyla raptiyeleyip tabutumu süsledikten sonra başlıklarında, AS.İZ yazılı, 6 askerin omuzlamasıyla, naaşımı Diyarbakır’a, gönderiyorlar ve oradan da baba ocağı olan ana kucağına çok sürmeden şehit cenazesini, taşıyan ambulans, evimizin sokağında, ortalığı ayağa, kaldıran siren, sesleriyle ve arkasına taktığı kalabalıkla belirleyin veriyor. —Babam…
emekli insan genelde evin bekçisi olduğundan, ambulansı ilk gören babam olurdu, korkunun gerçeğe dönüştüğü gün gelip çattığından, 'oğlum' diye feryadın kopmasıyla olduğu yere yığılı verirdi, çünkü zayıf bedeni vardı, 25 yaşındaki evladının şehit oluşuna hangi baba dayanabilirdi ki? kaybetmenin esiri olurcasına tabutumun önünde tükenir kalırdı ve sigarasını içerdi diğer eliyle de devrilmiş gözlerinden akan gözyaşlarını silerdi.  —Annem…
bebesini dokuz ay bedeninde, yıllarca sırtında taşıyan ve sütü yetmediğinden kapı kapı koşturan karşılıksız sevgisiyle çocuğunu kendisine, âşık ettirmesini bilen ve ne kadar üzülürse üzülsün, mutlu kadın rolünü yapabilen, her vakit elleri semaya açık olduğundan, hayattaki tek dua kapım ve gün görmemiş, gözümün nuru annem. Metanetini, korumasını bilirdi, naaşımın başında, kimselere yaslanmadan, dimdik ayakta durabilirdi düşman sevinmesin diye belki de arkamdan damla gözyaşı dökmezdi.

—Eşim…
acılar içerisindeki bedeni naaşım karşısında, güçsüz düşecekti bayrağa sarılmış tabutuma sıkı sıkı kapanarak, gözyaşları içerisinde öpüp öpüp çırpınırcasına önünde, yığılıp kalacaktı, yokluğumun vermiş olduğu acı hayatını karartarak, onu da perişan edecekti, sevdiğinden ayrılmanın acısıyla, dünyası başına yıkılacaktı, kahpe bir kurşun yüzünden, hayalleri yarım kalacağından beni özleyecekti eksikliğimi beter acılarla hissedecekti;
—'şehit düşeceğin kalbime doğmuştu ama korkumdan söyleyemiyordum' diye, mezarımın başında ağıtlar yakacaktı, el ayak çekilince, gecenin kasvetinin beraberinde getirdiği karanlığında tek başına ağlayacaktı nede olsa sevenlerim için, benden geriye kalan, mukaddes emanetimiz olan oğuzu bedeninde taşıyordu bensiz çocuğumuza ana baba, dağ ve bağ olacaktı, onun için elbette ki zor olacaktı, belki her şey bitecek, belki de yaşanacak çileli yaşamın anlamı dahi kalmayacaktı çünkü varlığıma alıştığından yokluğuma alışamayacağından eksik bir ömrü acılar içerisinde yaşayacaktı.</p>
