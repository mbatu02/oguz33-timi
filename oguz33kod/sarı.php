<p>Aileme sevdiklerime ve kendime yaşattığım kabus gibi acılara daha fazla dayanamadığımdan tüm kuvvetimi toplayarak sarı renkli ceset torbasında kan ter içerisinde, çıkmayı başardım ve çevreme, göz gezdirdikten sonra dolu dolu nefes aldım kendimi adeta anamdan yeni doğmuş gibi hissettim. O gecenin, nasıl geçtiğini ve o geceye ne şekilde, ağladığımı bilmiyorum, bildiğim ceset torbası içerisinde, kaldığım müddetçe, yüce mevla’nın beni sevenlerime bağışlaması için acımasız dağların koynunda saklamasaydı. Sonuçta özgür hayal gücüden yola çıkılarak karakterleriyle birlikte özenle yazmış olduğum ve kendi üslubumla sahnelediğim ölüm oyunum bittiğinde huzur bulmuştum mal mülk gözümde pul olmuştu evet belki bedenimden kopan bir parça ceset torbasında duruyordu, belki de çevremdeki, insanlara acıların hassını, yaşatmıştım ama yine de girmiş olduğum, kanlı çatışmada, ölmemiştim ve kâbus dolu, oyunumdan sonra kefeni yırtmıştım. Gerçek bir cenaze merasimindeki gibi herkesin acı çekmesi bedenime ağır geldi o an yüce mevlana sevdiklerime acımı gösterme diye dua ettim tabii böyle bir ölüm oyununu her babayiğit motor diyerek sahneleyemez çünkü bu senaryonun yazılabilmesi için cellat gibi başka canları yakmak gerekiyor. Bir gün kendi âlemimize gitmemiz için her şeyimizi bırakıp gideceğiz, her nefis ölümü elbet ki tadacaktır ama bu defa ki ölüm, benim için kara toprak olmamıştı, dünyadaki son gecemde olmamıştı tam tersi yepyeni başlangıç olmuştu adeta taze ve sıfır bir hayatın koynuna girmiş gibi bağışlık sistemim güçlenmişti ve beni savunmada tutarak hayatımın anlamını başka kılmıştı.

